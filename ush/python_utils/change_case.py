#!/usr/bin/env python3

def uppercase(str):
    """ Function to convert a given string to uppercase

    Args:
        str: the string
    Return:
        Uppercased str
    """
    
    return str.upper()


def lowercase(str):
    """ Function to convert a given string to lowercase

    Args:
        str: the string
    Return:
        Lowercase str
    """
    
    return str.lower()

